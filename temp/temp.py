# 建立一個空字典 (Hash Table)
scores = {}

# 或者也可以這樣寫 (效果一樣)
# scores = dict()

print("目前的聯絡簿:", scores)
# 輸出: 目前的聯絡簿: {}

# 新增三位學生的成績
scores["小明"] = 90
scores["小華"] = 85
scores["小美"] = 95

print("新增後的成績:", scores)
# 輸出: 新增後的成績: {'小明': 90, '小華': 85, '小美': 95}

# 1. 直接查詢 (如果確定名字存在)
print("小明的成績是:", scores["小明"])
# 輸出: 小明的成績是: 90

# 2. 安全查詢 (如果不確定名字在不在)
# 如果用 scores["大雄"] 會報錯 (KeyError)，因為大雄不在表裡。
# 所以我們用 .get() 方法，如果找不到，它會給一個預設值 (例如 None 或 "沒考")
print("大雄的成績是:", scores.get("大雄", "查無此人"))
# 輸出: 大雄的成績是: 查無此人
